// Map ASCII → font index
static int font_index(char c)
{
    if (c >= 'A' && c <= 'Z')
        return c - 'A';

    if (c == ' ')
        return 26;

    return 26; // fallback → space
}


// out must be preallocated large enough
// returns number of columns written
size_t text_to_bitmap(const char *text,
                      uint8_t *out,
                      size_t out_max)
{
    size_t pos = 0;

    for (size_t i = 0; text[i] != '\0'; i++) {

        char c = toupper((unsigned char)text[i]);
        int idx = font_index(c);

        // copy 5 columns of glyph
        for (int col = 0; col < 1; col++) {
            if (pos >= out_max) return pos;

            out[pos++] = seg7_font[idx][col];
        }

        // // add 1-column spacing between letters
        // if (pos < out_max) {
        //     out[pos++] = 0x00;
        // }
    }

    return pos;
}

